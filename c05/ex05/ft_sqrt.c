int	ft_sqrt(int nb)
{
	int	x;

	x = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (x <= nb / 2)
	{
		if (nb / x == x && nb % x == 0)
			return (x);
		x++;
	}
	return (0);
}
